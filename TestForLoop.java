package loops;

public class TestForLoop {

	public static void main(String[] args) 
	{
		System.out.println("Print 1 to 5 numbers");
		for(int i=1; i<=5; i++)
		{
			System.out.print(i + " ");
		}
		System.out.println();
		System.out.println("Print 1 to 5 numbers as a reverse ");
		for(int i=5; i>=1; i--)
		{
			System.out.print(i +" ");
		}
		

	}

}
